const TABLE_HEAD = [
  { id: 'name', label: 'Name of service', alignRight: false },
  { id: 'approvalWorkFlow', label: 'Approval Workflow', alignRight: false },
  // { id: 'formSchema', label: 'Form Schema', alignRight: false },
  { id: '' }
];

